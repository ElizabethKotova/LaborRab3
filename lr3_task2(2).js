function askAge() {
    let age;
    let confirmed = false;
    
    while (!confirmed) {
        // Запрашиваем возраст у пользователя
        age = prompt("Сколько вам лет?", "");
        
        // Если нажали "Отмена" в первом prompt, завершаем выполнение
        if (age === null) {
            return;
        }
        
        // Проверяем, что введено число
        if (isNaN(age) || age.trim() === '') {
            alert("Пожалуйста, введите число");
            continue;
        }
        
        // Уточняем возраст
        confirmed = confirm(`Вы уверены, что вам ${age}?`);
        
        // Если не подтвердили, цикл продолжится
    }
    
    // Если подтвердили, выводим сообщение
    alert(`Спасибо, ваш возраст ${age} лет сохранен`);
}

// Запускаем функцию при загрузке страницы
window.onload = askAge;